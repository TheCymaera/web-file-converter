<h1 class="font-bold text-2xl">File Converter</h1>
This app is a simple utility to convert files to other formats. Unlike many other converters, it runs entirely in your browser. Your files are never sent to my servers. <!--In fact, you can install it as a <a href="https://web.dev/progressive-web-apps/" target="_blank">PWA</a> and use it offline!-->
<br />
<br />

This tool is still in development and may not always work correctly.
You may report issues or give feedback through <a class="text-primary-500 underline" href="https://github.com/TheCymaera/web-file-converter/issues" target="_blank">GitHub</a>.

<br />
<br />

<h2 class="font-bold text-xl">How it works</h2>
PNGs, JPEGs, and WebPs are converted using HTML5 canvases. 

Videos and audio files are converted using the <a class="text-primary-500 underline" href="https://github.com/ffmpegwasm/ffmpeg.wasm" target="_blank">FFmpeg.wasm</a> library.
<br />
<br />

